  <h1>Purpose</h1>

  Display future (scheduled) or published posts in responsive grids using shortcodes. Works with PODS, Advanced Custom Fields, WooCommerce, and page builders like Elementor, Beaver Builder, Thrive Architect or WPBakery / Visual Composer. Plenty of customization options available either for power and expert users. Use the default theme or create your own theme.

  <!-- === === -->

  <br><br>
  <hr>

  <!-- === === -->

  <h1>Shortcode</h1>

  The Future Posts is a <a href='https://codex.wordpress.org/shortcode' target='_blank'>shortcode</a>. As such, it has the flexibility to be used on any page, post, product, template, footer, widget, etc. You can use it everywhere on your website, with as many different settings as you want, regardless of the theme you have installed. You can use it on WordPress standard text editor and virtually on any page builder of your choice. As you will see below, The Future Posts is very simple to use and to configure. Yet it features a wide range of flexible options, making it the most powerfull scheduled posts plugin on the WordPress Repository.

  <br><br>

  Use the following shortcode, without any attributes whatsoever, to display all future (scheduled) non-custom post types.


  <h1>WordPress text editor</h1>

  You can use The Future Posts shortcodes on any page or post using the WordPress text editor. Just insert the shortcode where you want the The Future Posts grid to appear. You can insert multiple instances of The Future Posts on the same page or post, each one with its own specific settings.

  <!-- === === -->

  <br><br>
  <hr>

  <!-- === === -->

  <h1>Widgets and Sidebars</h1>

  You can use The Future Posts shortcodes on sidebars and widget areas like footers. On the Appearance / Widgets admin page just use the Text widget or the Custom HTML widget to insert the shortcode(s).

